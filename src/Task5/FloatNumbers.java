package Task5;

public class FloatNumbers {
    public static void main(String[] args) {

        float a = 3.5F;
        float b = 12.78F;
        float c = -1.08F;
        int x = -5;
        int y = 5;


        if((x <= a) && (a <= y)) {
            System.out.println("Number a belongs to the range.");
        } else {
            System.out.println("Number a does not belong to the range.");
        } if ((x <= b) && (b <= y)) {
        System.out.println("Number b belongs to the range.");
        } else {
            System.out.println("Number b does not belong to the range.");
        } if ((x <= c) && (c <= y)) {
            System.out.println("Number c belongs to the range.");
        } else {
            System.out.println("Number c does not belong to the range.");
        }
    }
}


